const makeMetric = (
  metricCode: string,
  metricName: string,
  phase: string,
  phaseName: string,
  dataDimension: string,
  dataDimensionName: string,
  evalTarget: string,
  evalTargetName: string,
  calcScore: number,
  actualScore: number,
  riskLevel: RiskLevel,
  features: Record<string, unknown>,
  detailUrl: string,
): MetricVO => ({
  metricCode,
  metricName,
  phase,
  phaseName,
  dataDimension,
  dataDimensionName,
  evalTarget,
  evalTargetName,
  calcScore,
  actualScore,
  riskLevel,
  features,
  description: `基于版本数据的${metricName}评估。`,
  detailUrl,
});

const groupMetrics = (metrics: MetricVO[], keyName: keyof MetricVO, displayName: keyof MetricVO): GroupVO[] => {
  const groups = new Map<string, MetricVO[]>();

  for (const metric of metrics) {
    const key = String(metric[keyName]);
    groups.set(key, [...(groups.get(key) ?? []), metric]);
  }

  return Array.from(groups.entries()).map(([key, groupedMetrics]) => ({
    key,
    displayName: String(groupedMetrics[0][displayName]),
    groupScore: Math.round(groupedMetrics.reduce((sum, metric) => sum + metric.actualScore, 0) / groupedMetrics.length),
    metrics: groupedMetrics,
  }));
};

const makeMetrics = (patchId: number, riskLevel: RiskLevel, totalScore: number): MetricVO[] => {
  const riskGap = Math.max(1, Math.round((100 - totalScore) / 2));
  const defectNum = Math.max(0, Math.round((100 - totalScore) / 10));
  const coverageRate = Math.min(0.98, Math.max(0.35, totalScore / 100));
  const totalCases = 800 + (patchId % 5) * 20;
  const coveredCases = Math.round(totalCases * coverageRate);

  return [
    makeMetric(
      'CHANGES_RISK',
      '版本变更风险',
      'DEV',
      '开发阶段',
      'QUALITY',
      '质量风险',
      'PATCH',
      '版本',
      totalScore,
      Math.max(0, totalScore - 2),
      riskLevel,
      {
        changeCount: { value: riskGap },
        summaryReqConsistent: { value: riskLevel !== 'HIGH' },
        summaryCodeDiffCount: { value: riskGap },
      },
      '/detail/changes',
    ),
    makeMetric(
      'CASE_REVIEW_TIMELINESS',
      '用例评审及时性',
      'REQUIREMENT',
      '需求阶段',
      'EFFICIENCY',
      '交付效率',
      'PATCH',
      '版本',
      totalScore,
      Math.max(0, totalScore - 3),
      riskLevel,
      {
        reviewTime: { value: '2026-07-08T10:00:00' },
        testStartTime: { value: '2026-07-08T09:00:00' },
        delayDays: { value: riskLevel === 'HIGH' ? 2 : 0 },
      },
      '/detail/case-review',
    ),
    makeMetric(
      'DEFECT_RISK',
      '缺陷收敛风险',
      'TEST',
      '测试阶段',
      'QUALITY',
      '质量风险',
      'SYSTEM',
      '系统',
      totalScore,
      Math.max(0, totalScore - 5),
      riskLevel,
      {
        defectNum: { value: defectNum },
        testDay: { value: 3 + defectNum },
        totalDays: { value: 6 },
      },
      '/detail/defects',
    ),
    makeMetric(
      'SMART_TEST',
      '智能测试渗透率',
      'TEST',
      '测试阶段',
      'EFFICIENCY',
      '交付效率',
      'SYSTEM',
      '系统',
      totalScore,
      Math.max(0, totalScore - 6),
      riskLevel,
      {
        clickCounts: {
          案例生成: Math.max(1, Math.round(totalScore / 40)),
          缺陷分析: Math.max(1, Math.round((100 - totalScore) / 25)),
        },
      },
      '/detail/smart-test',
    ),
    makeMetric(
      'COVERAGE_390',
      '390回归覆盖率',
      'TEST',
      '测试阶段',
      'QUALITY',
      '质量风险',
      'SYSTEM',
      '系统',
      totalScore,
      Math.max(0, totalScore - 7),
      riskLevel,
      {
        num: { value: totalCases },
        cover: { value: coveredCases },
        coverageRate: { value: coverageRate },
      },
      '/detail/coverage-390',
    ),
    makeMetric(
      'COVERAGE_FUNCTION',
      '变更函数覆盖率',
      'RELEASE',
      '发布阶段',
      'QUALITY',
      '质量风险',
      'SYSTEM',
      '系统',
      totalScore,
      Math.max(0, totalScore - 8),
      riskLevel,
      {
        appLevel: { value: '核心系统' },
        javaCoverageInfo: {
          cover: Math.round(200 * coverageRate),
          num: 200,
          deletedNum: 2,
          artificialNum: 1,
          filterNum: 3,
          coverState: 'ALL_OPENED',
        },
        cCoverageInfo: {
          cover: Math.round(100 * coverageRate),
          num: 100,
          deletedNum: 1,
          artificialNum: 0,
          filterNum: 2,
          coverState: 'ALL_OPENED',
        },
      },
      '/detail/coverage-function',
    ),
  ];
};

const makePatchScore = (snapshot: ScoreSnapshot): PatchScoreVO => {
  const metrics = makeMetrics(snapshot.patchId, snapshot.riskLevel, snapshot.totalScore);

  return {
    patchId: snapshot.patchId,
    totalScore: snapshot.totalScore,
    qualityScore: snapshot.qualityScore,
    behaviorScore: snapshot.behaviorScore,
    riskLevel: snapshot.riskLevel,
    snapshotsTs: snapshot.snapshotsTs,
    summary: snapshot.summary,
    status: snapshot.status,
    releaseType: snapshot.releaseType,
    description: snapshot.description,
    sysId: snapshot.sysId,
    systemName: snapshot.systemName,
    systemKeyId: snapshot.systemKeyId,
    systemLevel: snapshot.systemLevel,
    teamName: snapshot.teamName,
    patchOwner: snapshot.patchOwner,
    patchOwnerId: snapshot.patchOwnerId,
    testLeader: snapshot.testLeader,
    testLeaderAccount: snapshot.testLeaderAccount,
    devLeader: snapshot.devLeader,
    devLeaderAccount: snapshot.devLeaderAccount,
    testOwner: snapshot.testOwner,
    testOwnerUserId: snapshot.testOwnerUserId,
    testOwnerGroup: snapshot.testOwnerGroup,
    planedTestFromTime: snapshot.planedTestFromTime,
    planedTestToTime: snapshot.planedTestToTime,
    planedIssueTime: snapshot.planedIssueTime,
    planedOnlineTime: snapshot.planedOnlineTime,
    auditTime: snapshot.auditTime,
    metrics,
    byPhase: groupMetrics(metrics, 'phase', 'phaseName'),
    byDataDimension: groupMetrics(metrics, 'dataDimension', 'dataDimensionName'),
    byEvalTarget: groupMetrics(metrics, 'evalTarget', 'evalTargetName'),
  };
};

